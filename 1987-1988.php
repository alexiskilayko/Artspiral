			<p><span class="date">December 4, 1987 – January 6, 1988</span></p>
			<p>This exhibition brings together three Asian American artists with unique visions. Curator Robert Lee writes, “The enormous wit that Bing, E’wao, and Kwong Chi bring to the modern spectacle is matched only by their high seriousness. To miss their Asianness is to miss the depth and range of their good ‘humors.’” Essay by Barry Schwabsky. <i>Learn more: <a href="http://artasiamerica.org/documents/5932/196" target="_blank">postcard</a>, <a href="http://artasiamerica.org/documents/1119/196" target="_blank">flyer</a>.</i> </p>
