			<h2>From Cambodia to the Bronx</h2>
			<p><span class="date">January – February 1988</span></p>
			<p>An exhibit of photographs documenting the Cambodian refugee community in the Bronx. When still in high school Leah Melnick befriended the community of boat people who survived and came to live nearby. She learned the Khmer language and became like family photographing their events and daily lives. After college she joined the staff at the United Nations. Organized by Robert Lee. <i>Learn more: <a href="http://artasiamerica.org/documents/2315/79" target="_blank">press release</a>, <a href="http://artasiamerica.org/documents/2317/79" target="_blank">letter</a>, <a href="http://artasiamerica.org/documents/2319/79" target="_blank">article</a>. </i></p>
			<h6>Participating artists:</h6>
