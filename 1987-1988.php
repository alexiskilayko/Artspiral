			<p><span class="date">May 20 – June 21, extended into July 1988</span></p>
			<p>A selection of sculptures, models, drawing and site plans proposed for specific locations in the Downtown/Chinatown community. A burgeoning Asian community in Lower Manhattan needs a new public image, an art that can embody the promise of change in Asian American relations. Curated by Robert Lee. Catalogue essays by John Yau, Peter Kwong, Kyong Park, intro by Robert Lee. <i>Learn more: <a href="http://artasiamerica.org/documents/8898/174" target="_blank">catalogue</a>, <a href="http://artasiamerica.org/documents/4900/174" target="_blank">proposal</a>, <a href="http://artasiamerica.org/documents/3238/174" target="_blank">press release</a>, <a href="http://artspiral.blogspot.com/2019/08/public-art-in-nyc-chinatown-gateways.html" target="_blank">blog</a>.</i></p>
