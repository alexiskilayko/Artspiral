      <h2>Gulp and Gallop!</h2>
      <p><span class="date">December 4, 1998 – January 16, 1999</span></p>
      <p>An exhibition presenting two artists, Ushio Shinohara and Rocky Kagoshima, as they create their art with the stubbornness of an idea arising directly from their experience of global cultural conflict. Their works express chaos in values and in energy, as well as shed light on Asian American sensibility in a postmodern world. Curated by Hideki Kawahara. <i>Learn more: <a href="http://artasiamerica.org/documents/3960/168" target="_blank">flyer</a>, <a href="http://artasiamerica.org/documents/3961/168" target="_blank">press release</a>.</i></p>
