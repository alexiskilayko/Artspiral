      <h2>7lb9oz: The Reintegration of Tradition into Contemporary Art</h2>
      <p><span class="date">March 26 – May 8, 1999</span></p>
      <p>An exhibition of four artists who each represent different approaches but are similar in that they all share Asia as their point of departure. They demonstrate a confidence embracing their Asian origins and traditions in reconciling their conundrum of identity, and in doing so, bridging in their way two seemingly irreconcilable cultures. Curated by Robert Lee. <i>Learn more: <a href="http://artasiamerica.org/documents/5161/177" target="_blank">flyer<a>, <a href="http://artasiamerica.org/documents/5160/28" target="_blank">press release</a>.</i></p>
