      <h2>8th AAAC Annual: The Day and Night Transparent</h2>
      <p><span class="date">October 16 – November 21, 1998</span></p>
      <p>Eighth in a series, this exhibition introduces individuals whose search for an artistic and visual language is the only bond that joins them together. In a complex society with multiple contradictory interests at work, it is their creativity that sustains their growth. The exhibition as a whole serves as an introduction to the broad and diverse concerns of artists who together compose a part of the contemporary presence of Asia in America. Organized by Young M. Park, Trinh Le, and Robert Lee. <i>Learn more: <a href="http://artasiamerica.org/documents/2807/45" target="_blank">flyer</a>, <a href="http://artasiamerica.org/documents/2808/45" target="_blank">press release</a>.</i></p>
