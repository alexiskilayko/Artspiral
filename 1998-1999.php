      <h2>EE! EE! EE! TRANSlation/plantation/formation</h2>
      <p><span class="date">August 14, 1999</span></p>
      <p>A sculptural performance. As ice melts, a great white ball rises… A collaboration between a Chinese poet, an American poet, and a sculptor, with an additional four women performers.  Collages of multi-layered, multi-faceted aspects in communcation combine visual elements and three spoken languages – Chinese, Taiwanese & English – to foreground human desire as a process of translation, transplantation, and transformation. Sponsored by Contemporary Arts International in Boston, Administered by AAAC. A free performance held in Bryant Park 42ed Street & 6th Ave, NYC.</p>
