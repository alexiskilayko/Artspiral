		<span class="tag">2009 &ndash; 2010</span>
			<h2>Out of the Archive: Process and Progress</h2>
			<p><span class="date">September 18 – October 30, 2009</span></p>
			<p>A special exhibition installation, a gallery talk, a 54 page color catalogue, with several essayists, based on the recently launched <a href="http://artasiamerica.org">digital archive</a>.</p>
			<p>Guest curator Angel Velasco Shaw selected four artists from a review of the 160 artists in artasiamerica.org. The printed catalogue has been edited and coordinated by Sarita Echavez See who chose four writers/literary scholars to review the artists' work: Karen Su, Karlyn Koh, and Jan Christian Bernabe.  In seeking to expand the ways in which AAAC has presented Asian American art in a community context, a critical writing approach was taken to broaden cultural criticism and interpret visual thinking.</p>
			<p>The essays shed light on these artists in a variety of ways such as: the artists' relationship to the work that they exhibited at AAAC in the past; the critique and contextualization of their current work; and the national and international context for these artists' creative production. These essays launch an investigation into the shifting rhetorics of art criticism and cultural criticism. The goal was to open up a critical dialogue for these artists/art works and a critical language for engaging more substantively with Asian American art. </p>
			<p>In the era of globalization, and the discourse associated with international art developments, the exploration of identity has come to be seen as passé in many sectors of the art world.  Yet the importance of visibility and recognition for Asian American artists, from the long view of history rather than the marketplace, continues.  In this light the Gallery Talk event of artists and writers held at White Box on Oct  7th was broadcast live on artasiamerica and remains accessible for <a href="http://www.ustream.tv/channel/out-of-the-archive-process-and-progress"> viewing online.</a> Catalogues can be purchased through a <a href="/support.php">donation</a> to the AAAC. A panel conversation on the exhibition and digital archive was held at the Tribeca Performing Arts Center on December 9, 2009.</p>
			<p>This was the last exhibition held in the gallery at 26 Bowery, AAAC’s home for thirty-three years.</p>
