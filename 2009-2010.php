			<h2>Out of the Archive: Process and Progress</h2>
			<p><span class="date">September 18 – October 30, 2009</span></p>
			<p>A special exhibition installation, a gallery talk, a 54 page color catalogue, with several essayists, based on the recently launched digital archive, artasiamerica.org.  This digital archive, a major undertaking three years in the making, consists of 10% of the total 1,500 Asian American artists entries in the original archive, which reflects important artists exhibited by AAAC since 1983, a sampling of 60 years and several generations of artistic innovations.</p>
			<p>Guest curator Angel Velasco Shaw selected four artists from a review of the 160 artists in artasiamerica.org. The printed catalogue has been edited and coordinated by Sarita Echavez See who chose four writers/literary scholars to review the artists' work: Karen Su, Karlyn Koh, and Jan Christian Bernabe.  In seeking to expand the ways in which AAAC has presented Asian American art in a community context, a critical writing approach was taken to broaded cultural criticism to interpret visual thinking.</p>
			<p>The essays shed light on these artists in a variety of ways such as: the artists' relationship to the work that they exhibited at AAAC in the past; the critique and contextualization of their current work; and the national and international context for these artists' creative production. These essays launch an investigation into the shifting rhetorics of art criticism and cultural criticism. The goal - open up a critical dialogue for these artists/art works and a critical language for engaging more substantively with Asian American art. </p>
			<p>In the era of globalization, and the discourse associated with international art developments, the exploration of identity has come to be seen as passé in many sectors of the art world. Yet the importance of visibility and recognition for Asian American artists, from the long view of history rather than the marketplace, continues. In this light the Gallery Talk event of artists and writers held at White Box on October 7 was broadcast live on artasiamerica and remains accessible for viewing.</p>
			<p>Curated by Angel Velasco Shaw. <i><a href="https://www.flickr.com/photos/asianamericanartscentre/albums/72157622510814089" target="_blank">Learn more</a>.</i></p>
