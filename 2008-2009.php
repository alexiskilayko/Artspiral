			<h2>America’s Chinatown Voices 藝匯唐人街</h2>
			<p><span class="date">May 9 – August 8, 2010</span></p>
			<p>Columbus Park becomes the site of a special outdoor art throughout the Summer where artists & volunteers install, rotate and maintain about 80 painted panels on which community people, children, artists and other New Yorkers can express themselves in written Chinese or English and painted images. People who live in Chinatown, as well as people who enjoy and care about this corner of New York City, are given the opportunity to speak their mind or simply to express their joy. <i>Learn more: <a href="https://www.flickr.com/photos/asianamericanartscentre/albums/72157618119632906" target="_blank">photo album 1</a>, <a href="https://www.flickr.com/photos/asianamericanartscentre/albums/72157623881782137" target="_blank">2</a>, <a href="https://www.flickr.com/photos/asianamericanartscentre/albums/72157624025562178" target="_blank">3</a>, <a href="https://www.nycgovparks.org/art/art14" target="_blank">NYC parks</a>, <a href="https://www.flickr.com/photos/asianamericanartscentre/4584064125/in/album-72157624006043804/" target="_blank">artwork</a>, <a href="http://artasiamerica.org/artist/detail/180" target="_blank">artist profile 1</a>, <a href="http://artasiamerica.org/artist/detail/181" target="_blank">2</a>.</i></p>
