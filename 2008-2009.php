			<h2>SPECIAL ARTSLAM</h2>
			<p><span class="date">July 8, 2009</span></p>
			<p>Artist Talk by Kerry Ann Lee. Kerry Ann Lee is an artist, designer and educator based in Wellington, New Zealand. Her work, “Home Made: Picturing Chinese Settlement in New Zealand,” celebrates an alternative cultural history through cut-paper, paint, found text and images. She presents both personal and local experiences of the Chinese settlement in New Zealand, those that the Chinese face behind the takeout counter as well as the home customs housed behind the plastic ribbon curtain. <i><a href="https://www.flickr.com/photos/asianamericanartscentre/albums/72157621899520607" target="_blank">Learn more</a>.</i></p>

			<h2>artasiamerica launch!</h2>
			<p><span class="date">July 28, 2009</span></p>
			<p>AAAC announces the digital archive, <a href="http://www.artasiamerica.org" target="_blank">artasiamerica.org</a>, marking the start of this expanding resource for the history of Asian American contemporary art.  It consists of historical images and documents focused on Asian American visual culture from 1945 to the present. The digital archive, selected from our physical archive of 1500 entries, is a professional collection of Asian/Asian American contemporary visual artists. The second phase will continue to cull the artists who were important in articulating the subject of Asian American Art and the issues of diversity in America.</p>
