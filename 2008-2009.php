			<h2>Teen Map Project</h2>
			<p><span class="date">November 2008</span></p>
			<p>AAAC Saturday Art School teacher Chi Lee managed and directed 17 teenagers between 18 and 21 years old from all over China at CMP to address the problem of making Chinatown a welcoming place for teens. They planned, researched, compiled agreements with shopkeepers as well as designed together a Map for Teens, locating twenty-two places welcoming teenagers in Chinatown. This was finalized and easily carried by folding into an origami shape. A collaboration between AAAC Saturday Art School & Chinatown Manpower Project (CMP).</p>
