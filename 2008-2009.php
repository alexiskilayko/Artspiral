			<h2>artasiamerica Launch</h2>
			<p><span class="date">July 28, 2009</span></p>
			<p>AAAC announces the digital archive, artasiamerica.org marking the start of this expanding resource for the history of Asian American contemporary art.  It consists of historical images and documents focused on Asian American visual culture from 1945 to the present. The digital archive, selected from our physical archive of 1500 entries, is a professional collection of Asian/Asian American contemporary visual artists. The second phase will continue to cull the artists who were important in articulating the subject of Asian American Art and the issues of diversity in America.</p>

