			<h2>Clusterbomb! Zero Capital Exhibition</h2>
			<p><span class="date">December 13 – January 15, 2009</span></p>
			<p>Clusterbomb! is a creative response to physical and psychological displacement in the post 9-11 era of disillusionment and inertia.  It is an interdisciplinary, intergenerational and transnational network of artists, writers, activists, cultural workers and youth. Participants are posing a collective challenge to why those who have are getting even more and those who don't have are getting even less, or nothing. A meeting at the closing of artists, stakeholders and interested parties included performances, new works, home cooked food, drinks and a discussion on the future Zero Capital international artists collaborations, events, and fundraising efforts. Artist Tomie Arai distributed signed, audience-selected Free Art chosen at the Opening. <i><a href="http://artasiamerica.org/documents/1357/74" target="_blank">Learn more</a>.</i></p>
