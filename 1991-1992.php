		<h2>Collection of Buddhas</h2>
		<p><span class="date">March 3 – extended to April 30, 1992</span></p>
		<p>A one-person exhibition showcasing the work of Ik-Joong Kang. The exhibition consists of 1,500 paintings on four square structures, each three-inch square and bearing an image of Buddha, some more unusual that others. The installation includes small, hidden-from-view loudspeakers of Buddhist monks chanting, enhancing the exhibit’s feeling of a Buddhist temple. Designed specifically for AAAC gallery space with dance company mirrors. Curarted by Robert Lee and Bing Lee. <i>Learn more: <a href="http://artasiamerica.org/documents/1847/26" target="_blank">postcard</a>, <a href="http://artasiamerica.org/documents/1850/26" target="_blank">press release</a>.</i></p>
