		<h2>Four Asian American Artists</h2>
		<p><span class="date">January 11 – February 16, 1992</span></p>
		<p>This exhibition centers on the complexity of ethical concerns among Asian American artists. Artists’ experiences can quietly retain a privacy that elongates time, separating itself from the noise of the marketplace, and may lead them to complex forms, with overlapping considerations, both ethical and paradoxical in tone. The paintings of Charles, Cyril, Tara, and Nguyen hold fast to this complexity of contemporary experience. In this they reflect an Asian American experience that is diverse, full, and very much another side of life and art in America. Curated by Robert Lee for the Council for the Arts on the North Shore. Held at Glen Cove, NY.</p>
		<h6>Participating artists:</h6>
		<ul class="list-columns">
			<li>Long Nguyen</li>
