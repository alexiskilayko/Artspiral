			<h2>boundLES</h2>
			<p><span class="date">November 27, 2007 - January 13, 2008</span></p>
			<p>Celebrating Contemporary Art On The Lower East Side. A collaborative of exhibition venues bringing together commercial galleries, nonprofit arts institutions, and artists in celebration of the rich creative spirit of the Lower East Side.  Forty-one participating organizations mounted events coinciding with the December 1 opening of the New Museum of Contemporary Art on the Bowery at Prince Street.</p>

			<h2>Rare Tribal Textiles from China</h2>
			<p><span class="date">December 20, 2007 – February 29, 2008</span></p>
			<p>An exhibition of Chinese Tribal Textiles from the Minority Peoples of China will be held through the Chistmas Holidays and Chinese Lunar New Year as well. These are museum quality fabrics representative of historical tribal traditions. They are from the peoples known as: Miao, Gejia, Yao, Yi, Dai, Li, and Zhang.  Many more garments, banners, festival jackets and blankets have filled the gallery, from Dai, Hainan as well as Gejia and Miao.  The collector of these tribal arts and textiles, Dr. Andrew Wang, spoke about this exquisite display of pattern and skill—the highest embroidery quality of China.</p>

			<h2>Yeong Gill Kim</h2>
			<p><span class="date">July 11 – August 1, 2008</span></p>
			<p>The complexity of Yeong Gill Kim’s brush work on muslin has attained a breathtaking clarity and simplicity.  He shows that contemporary art in America need not be founded on a break with the past, nor on claims of a new age of global technology.  He demonstrates that a crisis of identity, a seemingly unbridgeable gap born of two irreconcilable cultures, can be spanned with a renewed confidence in the vitality of one's Asian traditions. <i>Learn more: <a href="http://artasiamerica.org/documents/5159/28" target="_blank">poster</a>, <a href="http://artasiamerica.org/documents/5158/28" target="_blank">press release</a>, <a href="http://artasiamerica.org/documents/5157/28" target="_blank">artist statement</a>.</i></p>
