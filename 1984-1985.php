			<h2>Emily, Anna & Ti Shan: the First Generation</h2>
			<p><span class="date">March 8 – April 5, 1985</span></p>
			<p>These three artists together signify a direction Asians in America have come to take. All born and raised in the United States with little or no contact with their Asian heritage, they partake in a very American tradition of painting, yet add something unique of their own. Their works and styles bear many differences but are alike in their meditative tone. Curated by Robert Lee. <i>Learn more: <a href="http://artasiamerica.org/documents/943/156" target="_blank">catalog</a>, <a href="http://artasiamerica.org/documents/1334/156" target="_blank">press release</a>.</i></p>
