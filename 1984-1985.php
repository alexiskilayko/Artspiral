			<h2>Triangular Landscapes</h2>
			<p><span class="date">April 12 – May 10, 1985</span></p>
			<p>These three young Asian American women bring a particular sensibility to the man-made object and its accompanying space. Drawing upon both Asian and American traditions and employing man’s tool of geometry, the artists create unique and unusual works. “…Awesome powers to plan, manipulate, and control the natural environment…contrast sharply with the Chinese conception of a primal timeless landscape…” Quote by Robert Lee. </p>
