			<h2>Dreams and Fantasies</h2>
			<p><span class="date">May 17 – June 14, 1985</span></p>
			<p>In the work of Paul Wong, Helen Oji, and Ming Fay, nature is openly celebrated. Their dreams and fantasies are expressed by novel imagery, which releases a psychic drive, a source of energy and inner joy, an unhibited power contemporary society has sought to control, to hide, and even to deny. Something elemental, hidden from conventional pretenses that has eluded contemporary men and women, is expressed here. Essays by Barry Schwabsky and Robert Lee. Curated by Robert Lee. </p>
