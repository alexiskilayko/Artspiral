			<h2>People of China: Photos by Lee C. Lee</h2>
			<p><span class="date">February 1 - February 27, 1985</span></p>
			<p>Lee C. Lee (or Lee Charlotte Lee) was not only an artist but a pioneer of Asian American studies. The first woman professor of Asian ancestry at Cornell University, she helped initiate the institution’s Asian American Studies program, which was the first of its kind on the East Coast. Lee was known for her passion towards student needs, children’s welfare, and especially the cross-cultural issues between Americans and Asians. Her work is informed by such causes. </p>
