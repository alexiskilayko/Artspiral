			<h2>New York Eviction Blues</h2>
			<p><span class="date">March 31 – March 10, 2006</span></p>
			<p>Asian American Arts Centre (AAAC) fights eviction from its home of 28 years. This exhibition featured a select group of artists who have supported AAAC over many years. Many have a statement/story about their struggle with space, landlords, city regulations and real estate. The history of this urban struggle is reviewed, beginning in the 60s, to the passage of the Loft Law in 1982, to the current recognition among planners and government agencies of the central role the arts play in neighborhood revitalization. A panel talk was held on April 13th by Arlene Raven, art critic; Rebecca Goyette of First St. Studios; and Cathy Nanda of chashama. Another exhibition space, ABC No Rio, mounted “Eviction Blues,” an exhibition in solidarity with AAAC from April 21 to May 12, 2005. <i>Learn more: <a href="http://artasiamerica.org/documents/1488/201" target="_blank">flyer</a>, <a href="http://artasiamerica.org/documents/1510/201" target="_blank">press release</a>.</i></p>
