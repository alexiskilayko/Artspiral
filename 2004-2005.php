			<h2>Mei-ling Hom / Zhang Jian-jun at Mid Career</h2>
			<p><span class="date">May 27 – July 1, 2005</span></p>
			<p>Both Jian-Jun Zhang and Mei-ling Hom have chosen “culture” as a subject of their work and approached it from a personal perspective. This theme of culture defies concreteness and, like art, exists through space and time. It is this state that Mei-ling and Jian-Jun have come to investigate through their distinctive background experiences and unique philosophical beliefs. This exhibition then provides two apprehensions of art and culture that are distinct visually, yet alike in their basis upon the realization that art and culture are subject to constant fluctuation. Using different technical and material means, these artists provoke viewers to view inevitable change (rather than permanence) as the quality of existence. Essay by Judy Collischan, Ph.D. <i>Learn more: <a href="http://artasiamerica.org/documents/1782/127" target="_blank">flyer</a>, <a href="http://artasiamerica.org/documents/1789/127" target="_blank">press release</a>.</i></p>
