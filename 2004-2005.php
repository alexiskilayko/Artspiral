			<h2>The Topography of Absence</h2>
			<p><span class="date">November 19 – December 31, 2004</span></p>
			<p>This exhibition offers different ways artists delve into the landscape of their own experiences in an attempt to understand and grasp absence as fullness. Through work that offers viewers a series of quiet “meditations” of overlooked actions, impulses, and detritus, the empty trivial mundane moments of life are given an unexpected wonder. By examining the topography of their lives, artists find overlooked landscapes, not unlike the visual voyage through a Chinese landscape painting, providing an opportunity for us to re-assess what is truly important. Guest curated by Katarina Wong. <i>Learn more: <a href="http://artasiamerica.org/documents/5693/116" target="_blank">flyer</a>, <a href="http://artasiamerica.org/artist/detail/116" target="_blank">artist profile</a>.</i></p>
