			<h2>Ideas Chinatown</h2>
			<p><span class="date">May 2013</span></p>
			<p>Held in conjunction with the New Museum's Ideas Festival 2012. Sponsored by the New York Chinese Cultural Center with the Bowery Alliance of Neighbors and the Asian American Arts Centre. An evening of brief videos, illustrated talks, and performances presenting local Chinatown organizations and their work in the community. Also featured a panel discussion focusing on the effects of raising real estate values and its impacts on low-income or ethnic communities, including net losses of affordable housing, manufacturing, historical resources, and older local commerce in favor of chains and franchises. The discussion sought to consider what policies are needed to guide the Chinatown community through the challenges of gentrification.</p>

      <h2>Space Time: Presence</h2>
      <p><span class="date">May 3, 2013</span></p>
      <p>A talk mounted by Asian American Arts Centre in conjunction with the New Museum's Ideas Festival. Theories of Relativity and Quantum Mechanics are popular notions, yet Newtonian perceptions continue to prevail. In a cultural matrix where diverse cultural expressions are welcome, Asian Pacific American artists works can give insight into our era and the cultural horizons to be embraced. With profound changes to such fundamental notions, cultural issues are at stake here, along with implications for the protocols implemented in public space. This event proposes a dialogue between scientists, artists, and humanists on this question in the context of the new physics and considers their art in terms of how we experience and perceive - a lived sensibility. Held at the Christopher Henry Gallery, 127 Elizabeth St. Moderated by Professors Yibing Huang, literary scholar; tentatively Dr. R. Crease, Philosopher of Science; and Robert Lee. <i><a href="http://artspiral.blogspot.com/2011/05/festival-of-ideas-for-new-city-may-4-8.html" target="_blank">Learn more</a>.</i></p>
