			<p>A presentation of Asian American Arts Centre and the Cultural Equity Group (CEG) held as part of the New Museum's IDEAS CITY FESTIVAL. Sited in the pavilion located across Houston Street in the First Street Green, the event brought artists and members of CEG together to view art images and speak about an equitable Comprehensive Cultural Plan for New York City. Equity is finally on the agenda of NYC; now is the time for open public dialogue and to begin a process of understanding what “equity” will mean for neighborhoods, NYC, and the nation. CEG is a network of art organizations of color whose cultures represent many if not most of New York residents.</p>
