			<h2>Aboveground: 40 Moments of Transformation</h2>
			<p><span class="date">September 23 - 27, 2015</span></p>
			<p>A photography exhibition of young feminist activism in China. Forty specific actions in the struggle for gender equality were presented through a collaboration between China Rights in Action, Feminist Task Force, and Asian American Arts Centre. China's worsening repressive environment, bringing the fight for gender equality above ground comes at great personal risk. This exhibition frames and explores the determination with which these young feminists are pushing for a China with true gender equality. President Xi Jingping spoke on gender equality at the UN on the 27th on his "commitment to action." The Clemente Gallery on Suffolk St was the site of the exhibition and talks.</p>

			<h2>Hinterland Arts</h2>
			<p><span class="date">May 30, 2016</span></p>
			<p>Equity is finally on the agenda of NYC. Now is the time for open public dialogue and to begin a process to understand what "equity" will mean for neighborhoods, NYC, and the nation. A presentation of Asian American Arts Centre and the Cultural Equity Group (CEG) held as part of the New Museum's IDEAS CITY FESTIVAL. Brought artists and members of CEG together to view art images and speak about an equitable Comprehensive Cultural Plan for New York City. CEG is a network of art organizations of color whose cultures represent many if not most of New York residents. Sited in the pavilion located across Houston St in the First Street Green.</p>
