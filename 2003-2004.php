      <h2>Carol Sun / Nobuho Nagasawa at Mid Career</h2>
      <p><span class="date">May 22 – June 25, 2004</span></p>
      <p>Two artist’s installations. Carol Sun’s video installation builds on her grandfathers who never met, one influenced by the West, and brings the notion of legacy and family into a wider dialogue of communities and cultures. Nobuho Nagasawa’s "Mapping Chinatown: Making the Invisible Visible" collaborates with art students of SUNY Manhattan to print information about Chinatown on specially designed chopstick sleeves distributed to co-operating local restaurants. <i>Learn more: <a href="http://artasiamerica.org/documents/992/178" target="_blank">flyer</a>, <a href="http://artasiamerica.org/artist/detail/90" target="_blank">artist profile</a>.</i></p>
