      <h2>dream so much 2</h2>
      <p><span class="date">November 21, 2003 – January 9, 2004</span></p>
      <p>Japanese graffiti artists who have relocated to New York City are included in this exhibition of 2nd generation developments in graffiti. The artists’ works demonstrate fluid movement between overlapping cultures in the realm of the cultural hybrid, defying artificial definitions of cultural authenticity or straightforward appropriation. Guest Curated by Richard S. Chang and Alexandra Chang.</p>
