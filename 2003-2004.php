      <h2>In the Shadow of 9/11: A Chinatown Memorial Exhibition</h2>
      <p><span class="date">September 11 – October 15, extended to November 3, 2003</span></p>
      <p>An exhibition held in recognition of the events at the World Trade Center and the hardship experienced by the Chinatown community as a result of 9/11. This exhibition was created in collaboration between AAAC and SilkRoad Place to revive the attractiveness of Chinatown utilizing the asset of culture and the arts. Exhibited art works document what happened at the WTC site, the reaction in Chinatown itself, even the personal experience of being related to a professional who originally designed the Towers, among many other subjects. Held at The Gallery at Silk Road Place, 30 Mott Street. Organized by Bob Lee & Chee Wang Ng. <i>Learn more: <a href="http://artasiamerica.org/documents/2158/45" target="_blank">postcard</a>, <a href="http://artasiamerica.org/documents/2159/45" target="_blank">press release</a>.</i></p>
