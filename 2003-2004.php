     <h2>Mid Career: Carol Sun/Nobuho Nagasawa</h2>
			<p><span class="date">May 22 – June 25, 2004</span></p>
      <p>Nominating Panelists: Ko'an Jeff Baysa, Emily Cheng, Johnathan Goodman, Lilly Wei.</p>
      <p>Two artists' installations: Carol Sun's video installation builds on her grandfathers who never met, one influenced by the West, developing the notion of legacy and family into a wider dialogue of communities and cultures. Nobuho Nagasawa's "Mapping Chinatown: Making the Invisible Visible", collaborates with art students of SUNY Manhattan to print information about Chinatown on specially designed chopstick sleeves distributed to co-operating local restaurants. </p>
