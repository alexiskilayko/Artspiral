      <h2>Landscape and Memory</h2>
      <p><span class="date">March 19 – April 30, 2004</span></p>
      <p>Organized by Yoland Skeete & Hal Laessig as a collaboration with the Sumei Multidisciplinary Arts Center. Presented two artists whose works have developed in compelling, transcending ways. Ken Fukushima holds a scale up to experience, measuring one history to another, one culture to another, linking historical Japan to present thought. Wei Jane Chir’s abstractions seek sublime goals. Her measurements of mundane urban experience basks in the music of silence while Fukushima’s work exults in the music of time and its progeny and history. <i>Learn more: <a href="http://artasiamerica.org/documents/2618/52" target="_blank">flyer</a>, <a href="http://artasiamerica.org/documents/2623/52" target="_blank">press release</a>, <a href="http://artasiamerica.org/artist/detail/52" target="_blank">artist profile</a>.</i></p>
