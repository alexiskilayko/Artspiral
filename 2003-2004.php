      <h2>Landscape and Memory</h2>
			<p><span class="date">March 19 – April 30, 2004</span></p>
      <p>A collaboration with the Sumei Multidisciplinary Arts Center.</p>
      <p>Organized by Yoland Skeete & Hal Laessig .</p>
      <p>Two artists whose works have developed in compelling, transcending ways: Ken Fukushima links historical Japan to present thought. Wei Jane Chirs abstractions seek sublime goals. Her measurements of mundane urban experience basks in the music of silence while Fukushima's work exults in the music of time, and its progeny and history.</p>
