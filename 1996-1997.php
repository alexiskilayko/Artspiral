      <h2>Three Generations: Towards an Asian American Art History</h2>
			<p><span class="date">April 3 – May 17, 1997</span></p>
      <p>A mentoring relationship that spans the post WWII era to the present, from San Francisco to Oberlin to New York, links these three artists together. The style of one artist is unlike the other, each artist having different artistic goals and concerns. Furthermore, race was not the basis for one artist calling the other their mentor. We examine, then, how these artists are related to each other, as well as how they are related to other Asian American artists and how they contribute to a coherent theme of Asian American art. The Milieu theme informs this exhibition. Curated by Robert Lee. <i>Learn more: <a href="http://artasiamerica.org/documents/2714/46" target="_blank">flyer</a>, <a href="http://artasiamerica.org/documents/2715/46" target="_blank">press release</a>.</i></p>
