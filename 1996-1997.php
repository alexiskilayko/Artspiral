      <h2>Impressions: Contemporary Asian Artists Prints</h2>
			<p><span class="date">February 1 – April 11, 1997</span></p>
      <p>Publication by Brandywine Workshop in Philadelphia. Introduction by Allan Edmunds. Essay by Robert Lee, “Asian American Art: One Perspective.” This essay enabled the opportunity to write about the early years of Asian American art at AAAC. An exhibition of twenty-eight artists, either American immigrants or American-born, currently residing in the U.S. but linked by birth or ethnic heritage to Asia.  The exhibition offers a survey of the artists’ diverse styles, themes, and printing techniques and a window into contemporary graphic art as infused by ethnic perspectives and cultural and global crosscurrents.</p>
