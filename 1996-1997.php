			<h2>Milieu Part II</h2>
      <p><span class="date">November 22, 1996 - January 11, 1997</span></p>
      <p>Second in an exhibition series, Milieu: Part II brings together three Asian American artists who were active in the two decades of the post World War II era. It is based on a national research project begun in the mid 80s entitled, “Asian American Artists and Their Milieu: 1945-1965,” which documented nearly ninety artists and interviewed many. The aim is to trace the complex story of Asian American artists and the developing cultural presence of Asians in America. In this installment, we explore the participation of Asian American artists in the artistic movement of Abstract Expressionism—how they brought their own sensibility to the movement while establishing a connection to their Asian heritage. <i>Learn more: <a href="http://artasiamerica.org/documents/3985/169" target="_blank">flyer</a>, <a href="http://artasiamerica.org/documents/3992/169" target="_blank">press release</a>.</i></p>
