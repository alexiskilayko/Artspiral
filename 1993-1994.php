      <h2>Betrayal/Empowerment Part I</h2>
			<p><span class="date">April 18 – May 4, extended to June 10, 1994</span></p>
			<p>Exhibition of artists who began their careers in the 1950's, the 1960's, the 1970's, and the 1980's, some artists from Milieu included. Now that diverse peoples have embarked on the path of empowerment, it is appropriate to ask, what will restore our humanity beyond empowerment?  If we have access to the race for power and, perhaps, to win it, is this clearly our aim or is the race for power itself limited, even flawed? In seeking a humanity beyond empowerment, artists from each generation can recall for us cultural memories that, as Amalia Mesa Bains has said, "...allows us to assert our sense of continuity against all odds.” Held at Teacher’s College. Collaboration with Teacher’s College, Columbia University. Event included two-day conference. <i>Learn more: <a href="http://artasiamerica.org/documents/1623/109" target="_blank">flyer</a>, <a href="http://artasiamerica.org/documents/6150/109" target="_blank">press release</a>.</i></p>
