			<h2>Milieu Part I</h2>
			<p><span class="date">November 19 – December 29, 1993</span></p>
      <p>In the first of several exhibitions, Milieu: Part I brings together three senior Asian American artists who were active in the two decades of the post World War II era. Based on a national research project begun in the mid 80s entitled, “Asian American Artists and their Milieu 1945-1965,” which documented nearly ninety artists and interviewed many. This exhibition aims to bring to light cultural and artistic cross currents and to trace the complex story of Asian American artists. Each of the artists in the Milieu exhibition series shares a participation in the historical experience of Asians in America. We seek to document the existence of these artists, the predecessors to the young Asian American artists of today. Curated by Robert Lee. <i><a href="http://artasiamerica.org/documents/2185/193" target="_blank">Learn more</a></i>.</p>
