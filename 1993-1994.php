			<p>Now that diverse peoples have embarked on the path of empowerment, it is appropriate to ask, what will restore our humanity beyond empowerment?  If we have access to the race for power and, perhaps, to win it, is this clearly our aim or is the race for power itself limited, even flawed?   In seeking a humanity beyond empowerment, artists from each generation can recall for us cultural memories that, as Amalia Mesa Bains has said,  "....allows us to assert our sense of continuity against all odds".</p>
