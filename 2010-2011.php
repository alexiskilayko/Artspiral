			<h2>The Art of Danny Yung</h2>
			<p><span class="date">April 16, 2011</span></p>
			<p>Held at NYC Asian American Student Conference (NYCAASC). AAAC presented Danny Yung, introducing him, his Tian Tian Xian Shang blank boy canvas figure, and his book on Experimental Theatre. Honored by UNESCO's International Theatre Institute award in 2008, and by Germany for cultural exchange between Hong Kong and Germany, prizes for three top students were given, acknowledging Danny Yung as an art pioneer, particularly for his work to encourage the next stage of the evolution of Chinese Opera and the fresh development of Asia's traditional arts. (Largely unkown - he is the seminal figure in founding Basement Workshop in 1970 in New York Chinatown).</p>
			
