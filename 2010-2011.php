			<h2>Infinite Mirror: Images of American Identity</h2>
			<p><span class="date">January 25 – March 20, 2011</span></p>
			<p>The Infinite Mirror exhibition, sponsored by Artrain in conjunction with Brandywine and in partnership with International Arts & Artists, began its exhibition tour at Syracuse University. Infinite Mirror explores America’s image of itself through 65 diverse works of paintings, prints, photographs and video by 40 artists. Utilizing portraiture and figurative images, the exhibition tells the story of an array of triumphs, tragedies, relationships, and traditions that inform our notions of nationhood. The artwork is personal, reflective, and often autobiographical.  Curators are: Blake Bradford, director of education at the Barnes Foundation, Philadelphia; Robert Lee, executive director of the Asian American Arts Centre, New York; and Benito Huerta, associate professor & director of the Gallery at the University of Texas at Arlington. Asian American artists included are: Tomie Arai, Sung ho Choi, John Yoyogi Fortes, Eunjung Hwang, Indira Johnson, Swati Khurana, Kakyoung Lee, China Marks, Chunwoo Nam, Tara Sabharwal, and Scott Tsuchitani. Infinite Mirror traveling was scheduled through 2015. <i>Learn more: <a href="http://www.artrainusa.org/exhibitions.asp" target="_blank">Artrain</a>, <a href="http://www.brandywineworkshop.com" target="_blank">Brandywine</a>, <a href="http://www.artsandartists.org/exhibitions/infinitemirror.html" target="_blank">Infinite Mirror</a>.</i></p>
