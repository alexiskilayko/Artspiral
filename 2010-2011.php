			<h2>The Art of Danny Yung</h2>
			<p><span class="date">April 16, 2011</span></p>
			<p>AAAC presented Danny Yung, introducing him, his Tian Tian Xian Shang blank boy canvas figure, and his book on Experimental Theatre.  Having been honored with UNESCO’s International Theatre Institute award in 2008, as well as honored by Germany as one of three top students for cultural exchange between Hong Kong and Germany, Danny Yung is widely acknowledged as an art pioneer. He is particularly influential for his work in encouraging the next stage of the evolution of Chinese Opera and the fresh development of Asia’s traditional arts. (Largely unknown - he is the seminal figure in founding Basement Workshop in 1970 in NY Chinatown.) Held at NYC Asian American Student Conference (NYCAASC). <i>Learn more: <a href="http://artspiral.blogspot.com/2017/04/danny-yung-blank-boy-community-event-in.html" target="_blank">blog 1</a>, <a href="http://artspiral.blogspot.com/2014/07/danny-yungs-flower-plaque-installation.html" target="blank">2</a>, <a href="https://www.flickr.com/photos/asianamericanartscentre/4229707066/in/album-72157622978683669/" target="_blank">photograph</a>.</i></p>
