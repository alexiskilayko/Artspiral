			<h2>Boom Box</h2>
			<p><span class="date">March 16 – March 30, 2011</span></p>
			<p>An exhibition of two artists from AAAC’s digital archive, artasiamerica.org, and two artists unknown to this archive, chosen by Eun Young Choi. The exhibition explores the visual harmony inspired by nature’s Boom Box, overflowing with rhythms and melodies that combine together to create a complex yet intimate visual symphony. A collaboration with Clemente Soto Vélez Cultural Center. The gallery at 107 Suffolk Street was open seven days a week, 3:30 to 7pm. Curated by Eun Young Choi. <i><a href="http://artspiral.blogspot.com/2011/03/asian-american-arts-centre-presents.html" target="_blank">Learn more</a>.</i></p>
