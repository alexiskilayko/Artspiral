        <h2>Shaping Wholeness: Asian American Artists Create</h2>
        <p><span class="date">May 14 – 31, 2003</span></p>
        <p>To celebrate Asian American Heritage Month, an art exhibition of seven Asian American artists is presented. These artists give shape to wholeness. The wholeness of their being through art is expressed by each artist. This ability is a gift that is the special provenance of the creative disciplines. These are young artists, whose diversity in background, media, and outlook exemplify the Asian American community both in Queens and across the nation. This exhibition is designed to gather artists in and around Elmhurt Hospital to highlight and revitalize the local community. Sponsored by Elmhurst Hospital Center of the Queens Health Network. Curated by Robert Lee.</p>
