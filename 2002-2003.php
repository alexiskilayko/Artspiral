			<p>Selected documents in the document room from 1969 with the beginnings of the Asian American Movement up through to the present, touching upon many key moments, organizations, people, events, and ideas.  A treasure of over forty rare video tapes amplify the documents. For example: the choreography of Eleanor Yung, a 1983 Asian American Artist Panel talk, a 1988 installation by Epoxy, a 1991 interview of four Chinese artists by Alexandra Monroe, Nuo Mask Ritual Theatre in Jiangxi 1992, Eunjin Oh speaking at TAAC (The Assoc. of American Cultures) in 1994 in St. Louis, a 1996 CC Wang interview, a 1997 interview with VC Igarta, The NEA Tapes by Paul Lamarre 2000, Freud: In Search of Chinese Matter and Mind” 2001 By Zuni Icosahedron.  These and other artifacts assembled formed a context for the art works presented.</p>
			<p>Much of the work in selecting and organizing the documents was done with the assistance of an outside research consultant, Young M. Park, who has contributed a critical essay entitled, “The Asian American Arts Centre:  Its History of Reintegration”, dated June 2002.</p>

