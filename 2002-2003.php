        <h2>Below the Canal: After 9/11</h2>
        <p><span class="date">March 21 – May 2, 2003</span></p>
        <p>This exhibition is of artists who experienced 9/11 first hand as long time residents in Lower Manhattan and the Chinatown neighborhood. Their work can help to revitalize Lower Manhattan and place 9/11 in a context of local history.  The indigenous role and vital meaning of New York’s downtown should not be diminished by large scale undertakings. A panel talk of artists of Lower Manhattan was held on Tuesday, April 8: Grimanesa Amoros, Jane Freeman, Naoto Nakagawa, Paolo Pelosini, and Richard Rudich. <i>Learn more: <a href="http://artasiamerica.org/documents/6661/201" target="_blank">flyer</a>, <a href="http://artasiamerica.org/documents/6662/201" target="_blank">press release</a>.</i></p>
