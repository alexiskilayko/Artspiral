			<h2>Chinese Classical Architecture</h2>
			<p><span class="date">December 18, 2006 – March 16, 2007</span></p>
			<p>Artistic ornamental color painting on architectural structures shows the unique spirit and concept of China's classical building - an architectural culture of ornamental art. This art form first appeared on buildings during the Spring and Autumn Period (770-220 BC).</p>
			<p>The architectural ornamental art displays the beauty of Chinese folk art and high traditional culture as these have continuously influenced each other. Embedded in this ornament we can become aware of a collage of the many dimensions, and overlapping layers that integrate structural form with non structural elements of color, symbols, rebus art and pictorial imagery.  Principles of Fung Shui, Chinese philosophical notions, even Chinese medicine, are thoroughly embedded in traditional architecture and its orientation in space.  Multi-facet symbolism in design and motifs point to a way of life at peace with nature and natural processes, from which the contemporary goals of a sustainable society can learn.</p>
			<p>The organizer and researcher,  Ms. Chen Xiaorui, is a partner and Senior Designer of the China Architect Engineering and Consulting Company based in Beijing.</p>
