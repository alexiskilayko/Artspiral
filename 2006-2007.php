			<h2>Mixed Skin</h2>
			<p><span class="date">June 1 – July 13, 2007</span></p>
			<p>In this age of mixing cultural identity and ethnicities, how do we define ourselves? In "Mixed Skin," artists of mixed Asian descent & mixed African roots examine this question. Through interviews with people of mixed descent, Dorothy Imagire, an artist of Japanese and Iranian descent, created  a visual metaphor for the question with individual garments that blend  fabrics from different cultures. Accompanying her unique kimonos are books of personal quotes.  Kip Fulbeck's Hapa Project involves several hundred people: Fulbeck photographs those who identify as Hapa (or those who are partially Asian) and asks them to write about themselves. His performances and slide shows are similarly focused.  Toni Thomas does quilted imagery from her researches on the history of Chinatown in Newark NJ where some interracial marriages have been found.</p>
			<p>Organized by Robert Lee and Vikki Law, essays by Teresa Kim & Albert Chong.</p>
