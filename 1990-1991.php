			<h2>Violence of Victory</h2>
			<p><span class="date">April 18 – May 16, 1991</span></p>
			<p>An exhibition focusing on particular Asian groups’ experiences of violence in contemporary life as a result of war and aggression. The works also speak on the continuous presence of violence in our everyday life—a presence we take for granted. The title suggests that in winning the undercurrent of violence may leave its markings. The three featured photographers, in expressing their attitudes to violence, rely on contemporarary and historic experience significant to Asians in America. <i>Learn more: <a href="http://artasiamerica.org/documents/2069/143" target="_blank">flyer</a>, <a href="http://artasiamerica.org/documents/2070/143" target="_blank">press release</a>. </i></p>
