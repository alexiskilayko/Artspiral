			<h2>Come Let's Go View the Snow Until We're Buried</h2>
			<p><span class="date">April 26 - 27, 1991</span></p>
			<p>Tai Dang’s art is informed by his experiences as a Vietnamese refugee. This particular performance is inspired by a Japanese haiku and his desire to explore balance—balance between the spiritual and the physical, the East and the West, oneself and the material world. Directed by Andres Serrano. Sponsored by AAAC. Performance at Henry St. Settlement. <i>Learn more: <a href="http://artasiamerica.org/documents/6458/142" target="_blank">press release</a>, <a href="http://artasiamerica.org/documents/6456/142" target="_blank">postcard</a>, <a href="http://artasiamerica.org/documents/6457/142" target="_blank">flyer</a>.</i></p>
