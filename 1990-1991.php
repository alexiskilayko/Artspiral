			<h2>Focus on Artists from Hong Kong</h2>
			<p><span class="date">November – December 1990</span></p>
			<p>Featuring Choi Yan Chi, who painted one half of the gallery red, and two young artists—Comyn Mo and Pia Ho—who, as a pair, built a set and performed one night in the other half of the gallery. This was seven years before Hong Kong’s transfer to the mainland took place, when the artists residing in Hong Kong were still largely bypassed there in favor of important artists in the West. </p>
