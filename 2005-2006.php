			<h2>Detained</h2>
			<p><span class="date">March 24 – May 5, 2006</span></p>
			<p>An Arab American and Asian American exhibition aimed to bring two communities together around issues of race, exclusion, and spirituality. Chaplain James Yee, born and raised in New Jersey, chose to convert to Islam after serving in the US Army during the Gulf War, several years before being secretly arrested while serving at Camp Delta in Guantanamo Bay, Cuba, on allegations of espionage. He was detained for 76 days before all criminal charges were dropped. Aside from the issues raised by this incident are the underlying spiritual questions – an American, and an Asian American, graduate of West Point who chose to convert to Islam.  It is this personal narrative that may enable people to relate to each other, not just intellectually with information and knowledge but to see and encounter each other in a different light. Organized by Rabab Abdulhadi, Director, Center for Arab American Studies, University of Michigan, and Robert Lee, executive director of AAAC. Panel talks, etc., are projected with the assistance of the James Yee ad hoc Support Committee. <i>Learn more: <a href="http://artasiamerica.org/documents/2729/126" target="_blank">flyer</a>, <a href="http://artasiamerica.org/documents/2728/32" target="_blank">artist statement</a>.</i></p>
