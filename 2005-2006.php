			<h2>Mayumi Terada / Choong Sup Lim at Mid Career</h2>
			<p><span class="date">May 19 – June 24, 2006</span></p>
			<p>Choong Sup Lim deals with the dichotomies of east/west, interior/exterior, rural/urban, and nature/industry through the contradictions and paradoxes in his work. Forms and materials are carefully selected for their capacity to conjure multiple associations.  He has been working both in the United States and in Korea.  Since 2001, Mayumi Terada has been building dollhouse-sized interior spaces from balsa wood, styrofoam. and clay, then photographing and enlarging them into large silver gelatin prints. A sense of absence emerges from recent evidence of a presence in these sparsely-furnished rooms. Terada has lived in New York City for the past few years, exhibiting in Los Angeles, NYC, Korea, Tokyo and other cities throughout Japan. <i>Learn more: <a href="http://artasiamerica.org/documents/4865/201" target="_blank">flyer</a>, <a href="http://artasiamerica.org/documents/4866/201" target="_blank">press release</a>.</i></p>
