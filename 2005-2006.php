			<h2>Chinatown Work 2006</h2>
			<p><span class="date">April 18 – May 21, 2006</span></p>
			<p>Sponsored by the artists with support from AAAC, Rebuild Chinatown Initiative, Explore Chinatown Campaign, and Chinatown Partnership LDC. Every evening from 7:30 to 11pm, the façade of HSBC Bank at 58 Bowery near Manhattan Bridge was the site for an interactive art installation.  Silhouettes of pedestrians mix the footage of interior work spaces with time-lapse exterior street images of unique areas that define New York’s Chinatown contemporary work culture.  The interactive and responsive installation prompts passerbys to participate and to recognize and celebrate this community and the working people who make it. <i><a href="http://eskyiu.com/chinatownwork/" target="_blank">Learn more</a>.</i></p>
