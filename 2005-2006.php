			<h2>Yoshiki Araki: Hiroshima Born</h2>
			<p><span class="date">July 7 – August 11, 2006</span></p>
			<p>Yoshiki Araki died in 2000. He was born in March of 1950. His mother, as a young teenager, went into what was left of Hiroshima, the ruins of that nuclear wasteland, to search for her father, last of an old Samari family, but never found him. As a young man Yoshiki did make it to New York studying at the Art Students League,. In 2000 he was evicted from his home and studio in Brooklyn of many years that was filled with his art works and preparing for many more.  Under the stress and tension of trying to find a new space to accomodate all that he had gathered and planned, within a month Araki became ill and had to be hospitalized. He died shortly thereafter. Today we have yet to come to terms with the inhumanity of Hiroshima. In the darkness of that moment, as a second generation survivor, Yoshiki sought its quest in his art. His work may still offer us clues as to a journey we have yet to take. Some of his paintings and sculptures that he left behind is preserved in the AAAC Permanent Collection. Curated by Robert Lee & Sayaka Araki.</p>
			<p>Hiroshima Legacy: The Art of Yoshiki Araki was a panel talk on August 6, 2006, featuring: Jesse Fuchs, artist; Tristan Wolski, artist; Raphael Mostel, Tibetan Singing Bowl Ensemble; Sayaka Araki; Peaceful Tomorrows; September 11 Families; David McReynolds, the War Resisters League; Robert Lee, Moderator.</p>
