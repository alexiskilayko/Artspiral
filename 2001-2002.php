			<p><strong>Panel Two: Funding Art: A Community's Infrastrucuture</strong>. The health and vitality of the spirit of a people is reflected and sustained by the Arts. That’s why in 1965 the National Endowment for the Arts was founded.  However, in 1995, Expansion Arts, that part of the Endowment that supported diverse communities, was eliminated.  What has been the effect on Asian American communities?  What actions if any on other levels of government have been taken to fill the gap that was left.  What is the future of arts funding in regards to diverse community development.</p>
