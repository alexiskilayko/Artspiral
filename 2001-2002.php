			<h2>The Players: Asian American Art</h2>
			<p>A conference co-sponsored by AAAC & Asian/Pacific/American Studies Program & Institute at New York University. Held at New York University Main Building, 100 Washington Sq. East.</p>
			<p><span class="date">June 1, 2002</span></p>
			<p>After World War II in the United States of 1945, many artists were challenged by the prospects of the post war years including artists of Asian background. A few, like Yun Gee, were awakened much earlier.  With them began the quest for a contemporary art, an art touched by a complexion all its own, and wrought by hands that imparted a value beyond its modern surface. Asian American Art raises a multitude of questions - the role of past traditions and sensibilities, the issue of quality vs. ethnicity, and the perspective of critical terms like multiculturalism. Such questions mark this stage of Asian American Art History seeking to compliment the fire artists’ passion bring in urging forward the quest.  The significance of Asian American Art is open to discovery.</p>
