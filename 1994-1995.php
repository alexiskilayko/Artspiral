          <h2>Ancestors: a Collaborative Project with Kenkeleba House</h2>
          <p><span class="date">March – May 1995</span></p>
          <p>This public exhibition was designed to encourage the act of paying homage, not only to one’s own ancestors but to those of our neighbors, the ancestors of another people. It pays tribute to all of America’s forebearers, whether they be Asian, African, European, Latin American, or Native American. By focusing on two, Asian and African American ancestors, it recognizes that the rites and beliefs of diverse people permeate society as a whole and that acts of homage are fundamental to the development of the sense of community.  “…we recognize the wealth of our heritage as Americans and encourage the act of paying homage to all the Ancestors of this Land.” Organized by Corrine Jennings & Robert Lee. Mounted at Kenkeleba House 214 East 2nd Street and at AAAC. <i>Learn more: <a href="http://artasiamerica.org/documents/1710/93" target="_blank">flyer</a>, <a href="http://artasiamerica.org/documents/1715/93" target="_blank">press release</a>.</i></p>
