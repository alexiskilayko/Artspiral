          <h2>4th AAAC Annual: Photography and Community</h2>
          <p><span class="date">November 17 – December 31, 1994</span></p>
          <p>These three photographers exemplify diverse approaches to community. A glimpse at these works in progress informs the current dialogue on identity and community. In her project “Picturing Asian American,” Mei-ling Hom distributed single-use cameras to one hundred individuals and invited them to record images that describe their Asian American identity, resulting in a panorama of Asian American life. Corky Lee has cultivated a multi-decade long career of recording the development of the Asian American community, having captured landmark moments with amicable reality. Wing Young Huie’s works capture the lives of residents in Frogtown, a once mostly white, working class neighborhood, now transformed into a multi-ethnic inner-city neighborhood.  Curated by Robert Lee. <i><a href="http://artasiamerica.org/documents/1797/200" target="_blank">Learn more.</a></i></p>
