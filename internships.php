			<p>Community organizations reflect the dynamics of their community. They retain their existence through an interlocking growth-relationship with their community, preserving their history and reinventing their creative cultures. Community arts organizations, unlike major institutions, take their spark of life from the tumult, confusion and anguish of an unstable existence suppressed by a racial and cultural majority. Such organizations negotiate a relationship between the mainstream and their community&#8217;s subculture. In seeking to institutionalize, they pass on their special outlook and characteristic procedures to the next generation of culture workers. Community arts organizations are a storehouse of racial and cultural knowledge unique to their context. The cultural work of diverse people provides an entry point, both for understanding this &#8220;real politic&#8221; dynamic, and for understanding the reality of difference. They are a gateway for artists, staff, members, volunteers, and audience, as well as interns, a window to see and grasp the dynamics of a subculture.</p>
