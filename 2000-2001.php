      <h2>Point Arabesque: Paintings by Charles Yuen</h2>
      <p><span class="date">May 11 - June 23, 2001</span></p>
      <p>Charles Yuen’s paintings are enigmatic, cryptic, odd, whimsical, and dark in an attempt to unsettle our expectations, to subvert and question appearances and identities. In Yuen’s wonderland of inspired nonsense and alternatives, there is nothing that does not have another meaning, another way of seeing the situation, another layer of ambiguity that keeps things open-ended and in perpetual question. Essay by Lilly Wei. Curated by Robert Lee. <i>Learn more: <a href="http://artasiamerica.org/documents/1585/25" target="_blank">flyer</a>, <a href="http://artasiamerica.org/documents/1590/25" target="_blank">press release</a>.</i></p>
