      <h2>Power Print</h2>
      <p><span class="date">July 13 - Aug 3, 2001</span></p>
      <p>This woodblock prints exhibition from Nepal reflects a view on modernity from a 3rd World nation that is shaped by the ancient Vedic language whose historic texts are crucial to the earliest beginnings of civilization. For Nepal this remains a living language today. Ram Kumar Panday through his own creative vision, has culled and combined these symbols and applied them to contemporary living. Woodblock prints by Ram Kumar Panday. Presented in collaboration with Ms. Yin Peet.</p>
