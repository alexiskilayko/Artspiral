			<h2>Invented Selves: Images of Asian American Identity</h2>
			<p><span class="date">December 1 – 21, 1988</span></p>
			<p>An exhibition featuring three young artists who each use self-imaging to explore their Asian American identity. Their works reject stereotypic representations of Asians in popular media yet offer honest, non-idealized commentary on the Asian American community. With guest curator Margo Machida. <i>Learn more: <a href="http://artasiamerica.org/documents/5803/141" target="_blank">press release</a>, <a href="http://artasiamerica.org/documents/5802/141" target="_blank">flyer</a>. </i></p>
