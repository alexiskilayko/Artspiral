			<h2>The Chinese Folk Heritage: Performance, Exhibition, Folk Art Sharing, Talks</h2>
			<p><span class="date">January 12 - February 16, 1989</span></p>
			<p>An exhibition culled from several collectors in New York. Included: rocks, titled “Sculpture of the Gods” by C.C. Wang; toys, puppets, embroidered clothing, and flour figures from the Yellow River Region; original door god paintings, wood figurine, paper cuts, and New Year Prints; utensils, lunch baskets, lacquer ware, and other household items. Folk performance/traditions on February 12 by Kuang Yu Fong, Jeffery Yuen, Kevin Yao, Yu Chung Cheng, Lui Sui Ming, Jenny Tsui. Talks on February 14 by Lily Yeh on folk toys of the Yellow River Region and Anne Lee of Leekan Design on collecting. <i><a href="http://artspiral.blogspot.com/2019/08/the-asian-american-arts-centre-its.html" target="_blank">Learn more</a>.</i></p>

