            <h2>Year of the Rooster: A People’s Folk Art Exhibition</h2>
            <p><span class="date">January 15 – February 20, 1993</span></p>
            <p>Selections from Prof Liu of papercuts, woodblock prints, folk paintings with Lu Min-quin dough figurines that prizes living vitality.  Sculptural energetic cuttings from Shaanxi & Hebei master grandmothers. Woodblocks from Hebei, Sichuan, Hunan, & Shandong. Together with bilingual notes, myths, & stories. Festival with calligraphy demonstrations, fortune telling, dough figurines, paper folding, Peking Opera face painting, & Chinese chess game demonstration.  Marking the major role of women in Chinese folk traditions.  Lectures by Qian Liu on January 26 and February 16, “Repelling Demons: the Tradition & the Masks,” reviewing his recent original field research. Festival Performance on January 24. Uncle Ng sang on February 21.</p>

            <h2>Yin Peet</h2>
            <p><span class="date">March 12 – April 16, 1993</span></p>
            <p>An exhibition of seal carvings, sculptures, and installations by Yin Peet through which she re-visits her past life and relates it to the life’s choices she has made as an Asian woman living in American society today. Her seal carvings reflect the idea that seals are a recognition of the importance of our existence in society since ancient times; her sculptures combine elements of Western antiquity with modern images of ripe fruit; her installation of a life chess board tells of the current state of her life using strategically positioned chess pieces. Curated by Robert Lee. <i><a href="http://artasiamerica.org/documents/3043/29" target="_blank">Learn more</a>.</i> </p>
