            <h2>And He Was Looking for Asia: Alternatives to the Story of Christopher Columbus Today</h2>
            <p><span class="date">September 25 – October 29, 1992</span></p>
            <p>An exhibition exploring the Migration Period, a neglected era in European history.</p>
            <p>A story that has haunted me for years, is the link between Asia and the West.  But it did not begin to come together for me until I learned about the art of the so called 'Migration Peoples'.  After so many years, fragments remain hopelessly scattered over vast stretches of history and language. Without the artifacts to meticulously piece these together, how can I relate to you a story so fugitive?   But I dare not delay further, to leave in the hands of professional historians, a tale that has been ignored and denied in the West for so long.</p>
            <p>Briefly: The master narrative of Western history, of European history is missing a major component - the Migration Peoples who settled through much of Europe, has remained central to its story. Who were these diverse nomadic peoples….?</p>
            <p>In the so called Dark Ages - what historian Michael Wood on PBS describes as the "barbarian becomes the Roman, forming the 'Barbarian West'".  Goths,  Visigoths, Merovingians, Avars, Huns, migration peoples of the 2ed to the 6th Century whose oral traditions and animism were suppressed by the literary traditions and religion of the Mediterranian.  Venerable Bead in the 8th C. taught that the peoples of the Barbarian West were chosen Christian heirs of the Roman Empire destined to lead humanity to the City of God.</p>
            <p>The art of polychrome fibula pins, the only remains today of the original traditions of the migration peoples. Picasso's bull is identical to migration peoples art. “Christopher Columbus wore a round fibula pin with studded semi precious stones worked with interlacery”, ...perhaps, yes say perhaps... </p>
            <p>In looking for Asia, the myths and perceptions of Asia were foremost: the Tree of Life motif, the fountain of youth, El Dorado, Hawaii’s paradise, Hitler's Valhalla, temple of racial purity.   Midst all this how did East-West become polar opposites? if in deep history there was contact and interest in Asia then there are cultural ties we live with yet to be discovered, their implications yet to be unraveled.</p>
            <p>“To fight the good fight is all that ennobles us, and gives us finally our triumph over this mortal coil.”  How often have you heard Capt. Kirk or Shakespeare say this?  Are not the ethics and traditions of battle in every moment of our Western lives, to push us beyond our limits.  A key image of our nomadic heritage:  the open road.  As the first former President Bush once termed it, “We’re not done yet!”.</p>
            <p>Vaclav Havel, Feb. 4, 1992 in the NYT:  "The fall of Communism can be regarded as a sign that modern thought - based on the premise that the world is objectively knowable, and that the knowledge so obtained can be absolutely generalized - has come to a final crisis. We are looking for an objective way out of the crisis of objectivity.... Things must once more be given a chance to present themselves as they are, to be perceived in their individuality.  We must see the pluralism of the world, and not bind it by seeking common denominators or reducing everything to a single common equation.  Soul, individual spirituality, first-hand personal insight into things; the courage to be oneself and go the way one's conscience points, humility in the face of the mysterious Order of Being, confidence in its natural direction and, above all, trust in one's own subjectivity as the principal link with the subjectivity of the world - these are the qualities that ...the future should cultivate."</p>
            <p>“I suddenly realized that the molecules in my body, and the molecules in the spacecraft and in my partners bodies were manufactured in … the furnaces of stars. Suddenly those were my molecules, not intellectually but viscerally.” (He points to his heart. Samadhi).  <a href="https://ultraculture.org/blog/2014/06/02/astronauts-spiritual-experiences/" target="_blank">The late Edgar Mitchell, astronaut.</a></p>
            <p>– Bob Lee, Curator. <i>Learn more: <a href="http://artasiamerica.org/documents/5721/67" target="_blank">press release</a>, <a href="http://artasiamerica.org/documents/5718/67" target="_blank">postcard</a>, <a href="http://artasiamerica.org/works/1138/67" target="_blank">artwork</a>.</i></p>
