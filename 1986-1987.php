			<p><span class="date">May 29 – June 23, 1987</span></p>
			<p>Fourth and last in a series, an exhibition on the concept of Self in transition. Artists of diverse backgrounds approach their changing self-conceptions. It is said in The Self Beseiged intro, consciousness evolves qualities of irony, satire, a critical imaginary of difference, a heroic effort to protect and empower ourselves. Each artist finds their own way to emerge from the siege. Altering what is as fundamental as one’s self concept, as the writer Russell Leong says, ‘brings the water home.’ Curated and introduction by Robert Lee. Catalogue essay by Russell Leong. <i><a href="http://artasiamerica.org/documents/8569/51" target="_blank">Learn more</a>.</i> </p>
